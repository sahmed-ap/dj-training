# while creating a variable we don't have to assign any type it will automatically determine the type 
# based on the value of the variable. Since python is e dynamically typed.
number = 10
print(number)

# Variables can be re-assigned any different data type again.
number = "Test string"
print(number)

# Assigning values to multiple variables
a, b, c = 5, 3.2, 'Hello'

print(a)  # prints 5
print(b)  # prints 3.2
print(c)  # prints Hello
